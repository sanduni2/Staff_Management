
public class ScheMamber {

	private String scheduleId, date, task, empId;

	public ScheMamber() {
		super();
	}

	public ScheMamber(String scheduleId, String date, String task, String empId) {
		super();
		this.scheduleId = scheduleId;
		this.date = date;
		this.task = task;
		this.empId = empId;
	}

	public String getScheduleId() {
		return scheduleId;
	}

	public void setScheduleId(String scheduleId) {
		this.scheduleId = scheduleId;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getTask() {
		return task;
	}

	public void setTask(String task) {
		this.task = task;
	}

	public String getEmpId() {
		return empId;
	}

	public void setEmpId(String empId) {
		this.empId = empId;
	}

	

	

	
	
}
